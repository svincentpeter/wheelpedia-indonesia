import fs from "node:fs";
import path from "node:path";
import https from "node:https";
import http from "node:http";

const ids = JSON.parse(fs.readFileSync("scripts/vehicle-ids.json", "utf8"));
const sourcesPath = "scripts/vehicle-image-sources.json";
const sources = fs.existsSync(sourcesPath)
  ? JSON.parse(fs.readFileSync(sourcesPath, "utf8"))
  : {};
const outDir = path.resolve("public/vehicles");
const fallback = path.resolve("public/placeholders/fallback-vehicle.jpg");
fs.mkdirSync(outDir, { recursive: true });

function fetchBuffer(url, redirects = 0) {
  return new Promise((resolve, reject) => {
    if (redirects > 5) {
      reject(new Error("Too many redirects"));
      return;
    }
    const lib = url.startsWith("https") ? https : http;
    const req = lib.get(
      url,
      {
        headers: {
          "User-Agent": "WheelpediaImageBot/1.0 (personal learning app)",
          Accept: "image/*,*/*",
        },
      },
      (res) => {
        if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
          const next = new URL(res.headers.location, url).toString();
          fetchBuffer(next, redirects + 1).then(resolve, reject);
          return;
        }
        if (res.statusCode !== 200) {
          reject(new Error(`HTTP ${res.statusCode} for ${url}`));
          res.resume();
          return;
        }
        const chunks = [];
        res.on("data", (c) => chunks.push(c));
        res.on("end", () => resolve(Buffer.concat(chunks)));
      },
    );
    req.on("error", reject);
    req.setTimeout(30000, () => {
      req.destroy(new Error("timeout"));
    });
  });
}

async function ensureFallback() {
  if (fs.existsSync(fallback)) return;
  fs.mkdirSync(path.dirname(fallback), { recursive: true });
  const url =
    "https://images.unsplash.com/photo-1492144534655-ae79c964c9d7?w=800&q=80";
  const buf = await fetchBuffer(url);
  fs.writeFileSync(fallback, buf);
}

async function main() {
  await ensureFallback();
  const attribution = [
    "# Image attribution",
    "",
    "Generated by `scripts/download-vehicle-images.mjs`.",
    "Prefer free licenses (Unsplash, Pexels, Wikimedia).",
    "",
  ];
  for (const id of ids) {
    const dest = path.join(outDir, `${id}.jpg`);
    const src = sources[id];
    try {
      if (src?.url) {
        const buf = await fetchBuffer(src.url);
        if (buf.length < 1000) throw new Error("file too small");
        fs.writeFileSync(dest, buf);
        attribution.push(`- \`${id}.jpg\`: ${src.attribution || src.url}`);
        console.log("OK", id);
      } else {
        fs.copyFileSync(fallback, dest);
        attribution.push(`- \`${id}.jpg\`: fallback placeholder (Unsplash car stock)`);
        console.log("FALLBACK", id);
      }
    } catch (e) {
      fs.copyFileSync(fallback, dest);
      console.log("ERR→FALLBACK", id, e.message);
      attribution.push(`- \`${id}.jpg\`: fallback (error: ${e.message})`);
    }
  }
  fs.writeFileSync("public/ATTRIBUTION.md", attribution.join("\n") + "\n");
  console.log("Done. Wrote", ids.length, "images.");
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
