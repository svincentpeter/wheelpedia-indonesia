export const BODY_TYPES = [...new Set(VEHICLES.map((v) => v.bodyType))].sort();
export const PCD_LIST = [...new Set(VEHICLES.map((v) => v.pcd))].sort();

export function getVehicleById(id: string) {
  return VEHICLES.find((v) => v.id === id);
}

export function getVehiclesByBrand(brand: string) {
  return VEHICLES.filter((v) => v.brand === brand);
}

export function getVehiclesByPCD(pcd: string) {
  return VEHICLES.filter((v) => v.pcd === pcd);
}

export function searchVehicles(query: string) {
  const q = query.toLowerCase();
  return VEHICLES.filter(
    (v) =>
      v.brand.toLowerCase().includes(q) ||
      v.model.toLowerCase().includes(q) ||
      v.oemTire.toLowerCase().includes(q) ||
      v.pcd.toLowerCase().includes(q) ||
      v.bodyType.toLowerCase().includes(q)
  );
}
